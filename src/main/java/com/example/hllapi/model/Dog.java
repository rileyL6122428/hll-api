package com.example.hllapi.model;

public class Dog {
	
	public String name;
	public String bark;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getBark() {
		return bark;
	}
	
	public void setBark(String bark) {
		this.bark = bark;
	}		
	
}
